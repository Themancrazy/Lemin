#include "libft.h"

int		ft_atoi(const char *str)
{
	int							i;
	unsigned long long int		num;
	int							sign;

	i = 0;
	num = 0;
	sign = 1;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '+' || str[i] == '-')
		if (str[i++] == '-')
			sign = -1;
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = num * 10 + (str[i] - '0');
		i++;
	}
	if (num > 9223372036854775807 && sign == -1)
		return (0);
	else if (num > 9223372036854775807)
		return (-1);
	return (sign * num);
}
